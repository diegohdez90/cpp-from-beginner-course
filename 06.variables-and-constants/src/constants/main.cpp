// constants are values that cannot be changed after they have been initialized
// constants are useful for values that we want to use throughout our program but do not want to accidentally change
// in C++, we can declare constants using the "const" keyword
// when we declare a constant, we must initialize it with a value at the time of declaration, and we cannot change that value later in the program
// constants can be of any data type, including built-in types like int, double, char, and user-defined types like classes and structs
// using constants can help improve the readability and maintainability of our code, as it makes it clear that certain values are meant to be constant and should not be modified
const double pi = 3.14159;
